package lmsierra.com.myweather.model;

public class Suggestion {

    private String title;
    private long id;

    public Suggestion(String title) {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }
}